    private void loadBang(){
        for(int i=tblBang.getRowCount()-1; i>=0; i--){
            dtm.removeRow(i);
        }
        ConnectionDB conn=new ConnectionDB();
        for(BangDonHangObject bang:conn.dsChiTietDonHang()){
            Vector<Object>vec=new Vector<>();
            vec.add(bang.getMaDH());
            vec.add(bang.getNgayDat());
            vec.add(bang.getMaNCC());
            vec.add(bang.getMaSP());
            vec.add(bang.getSlDat());
            dtm.addRow(vec);
        }
    }
    private void loadCombo(){
        ConnectionDB conn=new ConnectionDB();
        cboNhaCC.removeAllItems();
        for(NhaCCObject ncc : conn.dsNhaCungCap()){
            cboNhaCC.addItem(ncc);
        }
        
        cboMaDonHang.removeAllItems();
        for(DonHangObject dh:conn.dsDonHang()){
            cboMaDonHang.addItem(dh);
        }
        
        cboMaSanPham.removeAllItems();
        for(SanPhamObject sp:conn.dsSanPham()){
            cboMaSanPham.addItem(sp);
        }
    }
