    private void btnTaoDonHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoDonHangActionPerformed
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy/MM/dd");
        String ma=txtMaDH.getText();
        String ngay=sdf.format(dateNgayDat.getDate());
        NhaCCObject ncc=(NhaCCObject) cboNhaCC.getSelectedItem();
        String manCC=ncc.getMa();
        ConnectionDB conn=new ConnectionDB();
        if(conn.themDonHang(ma, ngay, manCC)){
            JOptionPane.showMessageDialog(null, "Tạo đơn hàng thành công !");
            loadCombo();
        }
        else{
            JOptionPane.showMessageDialog(null, "Tạo đơn hàng thất bại !!!");
            return;
        }
    }//GEN-LAST:event_btnTaoDonHangActionPerformed

    private void btnDatHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDatHangActionPerformed
        DonHangObject dh=(DonHangObject) cboMaDonHang.getSelectedItem();
        SanPhamObject sp=(SanPhamObject) cboMaSanPham.getSelectedItem();
        int sl= Integer.parseInt(txtSoLuong.getText());
        ConnectionDB cnn=new ConnectionDB();
        if(cnn.themChiTietDatHang(dh.getMaDH(), sp.getMaSP(), sl)){
            JOptionPane.showMessageDialog(null, "Đặt hàng thành công ! !");
            loadBang();
        }
        else{
            JOptionPane.showMessageDialog(null, "Đặt hàng thất bại !!!");
            return;
        }
    }//GEN-LAST:event_btnDatHangActionPerformed

    private void btnXoaDonHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaDonHangActionPerformed
        ConnectionDB cnn=new ConnectionDB();
        DonHangObject dh=(DonHangObject) cboMaDonHang.getSelectedItem();
        int cf=JOptionPane.showConfirmDialog(null, "Bạn có chắc xoá đơn hàng", "Xác nhận xoá", JOptionPane.OK_CANCEL_OPTION);
        if(cf==JOptionPane.OK_OPTION){
            cnn.xoaPhieuNhapTheoMaHD(dh.getMaDH());
            cnn.xoaChiTietDatHang(dh.getMaDH());
            if(cnn.xoaDonHang(dh.getMaDH())){
                JOptionPane.showMessageDialog(null, "Xoá đơn hàng thành công !");
                loadBang();
                loadCombo();
            }
            else{
                JOptionPane.showMessageDialog(null, "Đặt hàng thất bại !!!");
                return;
            }
        }
        else{
            return;
        }
    }//GEN-LAST:event_btnXoaDonHangActionPerformed

