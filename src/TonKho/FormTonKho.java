    private void btnKiemTraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKiemTraActionPerformed
        SimpleDateFormat sdf=new SimpleDateFormat("ddMMyyyy");
        String ma=sdf.format(Date.from(Instant.now()));
        SanPhamObject sp=(SanPhamObject) cboSanPham.getSelectedItem();
        ConnectionDB cnn=new ConnectionDB();
        if(cnn.themTonKho(ma, sp.getMaSP(), cnn.slNhap(sp.getMaSP()), cnn.slXUAT(sp.getMaSP()))){
            JOptionPane.showMessageDialog(null, "Cập nhật thành công !");
            loadBang();
        }
        else{
            JOptionPane.showMessageDialog(null, "Cập nhật thất bại !");
            return;
        }
    }//GEN-LAST:event_btnKiemTraActionPerformed

    private void tblTonKhoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTonKhoMouseClicked
        int row=tblTonKho.getSelectedRow();
        if(row!=-1){
            tonKhoChon=new TonKhoObject(tblTonKho.getValueAt(row, 0).toString(),
                    tblTonKho.getValueAt(row, 1).toString(),
                    Integer.parseInt(tblTonKho.getValueAt(row, 2).toString()),
                    Integer.parseInt(tblTonKho.getValueAt(row, 3).toString()));
        }
    }//GEN-LAST:event_tblTonKhoMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if(tonKhoChon!=null){
            ConnectionDB cnn=new ConnectionDB();
            if(cnn.xoaTonKho(tonKhoChon.getMa())){
                loadBang();
            }
            else{
                return;
            }
        }
        else{
            return;
        }
    }//GEN-LAST:event_btnXoaActionPerformed

