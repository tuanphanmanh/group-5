    private void loadCombo(){
        cboSanPham.removeAllItems();
        ConnectionDB cnn=new ConnectionDB();
        for(SanPhamObject sp:cnn.dsSanPham()){
            cboSanPham.addItem(sp);
        }
    }
    private void loadBang(){
        ConnectionDB cnn=new ConnectionDB();
        for(int i=tblTonKho.getRowCount()-1; i>=0; i--){
            dtm.removeRow(i);
        }
        for(TonKhoObject tk:cnn.dsTonKho()){
            Vector<Object>vec=new Vector<>();
            vec.add(tk.getMa());
            vec.add(tk.getMaSP());
            vec.add(tk.getSlNhap());
            vec.add(tk.getSlXuat());
            dtm.addRow(vec);
        }
    }
