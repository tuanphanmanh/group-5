    private void loadCombo(){
        ConnectionDB conn=new ConnectionDB();
        cboPhieuXuat.removeAllItems();
        for(ThanhToanObject tt:conn.dsThanhToan()){
            cboPhieuXuat.addItem(tt);
        }
        cboSanPham.removeAllItems();
        for(SanPhamObject sp:conn.dsSanPham()){
            cboSanPham.addItem(sp);
        }
    }
    private void loadBang(){
        for(int i=tblPhieuXuat.getRowCount()-1; i>=0; i--){
            dtm.removeRow(i);
        }
        ConnectionDB cnn=new ConnectionDB();
        for(PhieuXuatObjet p :cnn.dsPhieuXuat()){
            Vector<Object>vec=new Vector<>();
            vec.add(p.getMaPhieu());
            vec.add(p.getNgay());
            vec.add(p.getTenKH());
            vec.add(p.getMaSP());
            vec.add(p.getSl());
            vec.add(p.getDonGia());
            dtm.addRow(vec);
        }
    }
