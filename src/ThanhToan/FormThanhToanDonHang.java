    private void btnTaoPhieuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoPhieuActionPerformed
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy/MM/dd");
        String ma=txtMaPhieu.getText();
        String ngayX=sdf.format(dateNgayX.getDate());
        String tenKH=txtTenKH.getText();
        ConnectionDB cnn=new ConnectionDB();
        if(cnn.themPhieuXuat(ma, ngayX, tenKH)){
            JOptionPane.showMessageDialog(null, "Tạo phiếu xuất thành công!!");
            loadCombo();
        }
        else{
            JOptionPane.showMessageDialog(null, "Tạo phiếu xuất thất bại");
            return;
        }
    }//GEN-LAST:event_btnTaoPhieuActionPerformed

    private void btnXoaPhieuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaPhieuActionPerformed
        ThanhToanObject tt=(ThanhToanObject) cboPhieuXuat.getSelectedItem();
        if(tt!=null){
            ConnectionDB cnn=new ConnectionDB();
            String ma=tt.getMaPhieu();
            int cf=JOptionPane.showConfirmDialog(null, "Bạn coá chắc xoá phiếu không?", "Xác nhận xoá", JOptionPane.YES_NO_OPTION);
            if(cf==JOptionPane.YES_OPTION){
                if(cnn.xoaPhieuXuat(ma)){
                    JOptionPane.showMessageDialog(null, "Xoá thành công !");
                    loadCombo();
                    loadBang();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Xoá thất bại !");
                    return;
                }
                
            }
            else{
                
                return;
            }
        }
        else{
            return;
        }
    }//GEN-LAST:event_btnXoaPhieuActionPerformed

    private void btnXuatHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatHangActionPerformed
        SanPhamObject sp=(SanPhamObject) cboSanPham.getSelectedItem();
        ThanhToanObject tt=(ThanhToanObject) cboPhieuXuat.getSelectedItem();
        int slX=Integer.parseInt(txtSoLuongXuat.getText());
        float donGia=Float.parseFloat(txtDonGiaXuat.getText());
        double thanhTien=slX*donGia;
        ConnectionDB cnn=new ConnectionDB();
        if(cnn.themChiTietPhieuXuat(tt.getMaPhieu(), sp.getMaSP(), slX, donGia,thanhTien)){
            JOptionPane.showMessageDialog(null, "Xuất hàng thành công");
            loadBang();
        }
        else{
            JOptionPane.showMessageDialog(null, "Xuất hàng thất bại");
            return;
        }
    }//GEN-LAST:event_btnXuatHangActionPerformed

