    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String ma=txtMaSP.getText();
        String ten=txtTenSP1.getText();
        String dvTinh=txtDonViTinh.getText();
        int sl=Integer.parseInt(txtSoLuong.getText());
        SanPhamObject sp=new SanPhamObject(ma, ten,  dvTinh, sl);
        if(connected.suaSanPham(sp)){
            JOptionPane.showMessageDialog(null, "Sửa sản phẩm thành công !");
            loadBang();
        }
        else{
            JOptionPane.showMessageDialog(null, "Sửa sản phẩm thất bại !");
            return;
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String ma=txtMaSP.getText();
        int cf=JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn xoá mã "+ma+"?",
                "Xác nhận xoá", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(cf==JOptionPane.OK_OPTION){
            if(connected.xoaSanPham(ma)){
            JOptionPane.showMessageDialog(null, "Xoá sản phẩm thành công !");
            loadBang();
        }
        else{
            JOptionPane.showMessageDialog(null, "Xoá sản phẩm thất bại !");
            return;
        }
        }
        else{
            return;
        }
    }//GEN-LAST:event_btnXoaActionPerformed

