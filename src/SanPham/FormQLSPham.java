    private void btnTimSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimSPActionPerformed
        String search=txtTimKiem.getText();
        ArrayList<SanPhamObject>dsSanPham=connected.dsSanPham();
        for(int i=tblSanPham.getRowCount()-1; i>=0; i--){
            dtm.removeRow(i);
        }
        for(SanPhamObject sp:dsSanPham){
            if(sp.getTenSP().contains(search)||sp.getMaSP().contains(search)||sp.getDonviTinh().contains(search)){
            Vector<Object>vec=new Vector<>();
            vec.add(sp.getMaSP());
            vec.add(sp.getTenSP());
            vec.add(sp.getDonviTinh());
            vec.add(sp.getSoLuong());
            dtm.addRow(vec);
            }
        }
    }//GEN-LAST:event_btnTimSPActionPerformed

