    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ma=txtMaSP.getText();
        if (ma.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Mã sản phẩm không để trống");
            return;
        }
        String ten=txtTenSP1.getText();
        if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Tên sản phẩm không để trống");
            return;
        }
        
        String dvTinh=txtDonViTinh.getText();
         if (dvTinh.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Loại sản phẩm không để trống");
            return;
        }
        try{
              int sl=Integer.parseInt(txtSoLuong.getText());
            SanPhamObject sp=new SanPhamObject(ma, ten, dvTinh, sl);
            if(connected.themSanPham(sp)){
                JOptionPane.showMessageDialog(null, "Thêm sản phẩm thành công !");
                loadBang();
        }
        else{
            JOptionPane.showMessageDialog(null, "Thêm sản phẩm thất bại !");
            return;
        }
        }
        catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Yêu cầu nhập số và không để trống");
            return;
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        int row=tblSanPham.getSelectedRow();
        if(row!=-1){
            String ma=tblSanPham.getValueAt(row, 0).toString();
            String ten=tblSanPham.getValueAt(row, 1).toString();
            String dvTinh=tblSanPham.getValueAt(row, 2).toString();
            int sl=Integer.parseInt(tblSanPham.getValueAt(row, 3).toString());
            sanPhamChon=new SanPhamObject(ma, ten, dvTinh, sl);
            txtMaSP.setText(ma);
            txtTenSP1.setText(ten);
            txtDonViTinh.setText(dvTinh);
            txtSoLuong.setText(sl+"");
        }
    }//GEN-LAST:event_tblSanPhamMouseClicked

