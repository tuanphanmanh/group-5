    private void tblTaiKhoanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTaiKhoanMouseClicked
        int dongChon=tblTaiKhoan.getSelectedRow();
        if(dongChon!=-1){
            String maTK=tblTaiKhoan.getValueAt(dongChon, 0).toString();
            String tenDN=tblTaiKhoan.getValueAt(dongChon, 1).toString();
            String matKhau=tblTaiKhoan.getValueAt(dongChon, 2).toString();
            String hoTen=tblTaiKhoan.getValueAt(dongChon, 3).toString();
            String sdt=tblTaiKhoan.getValueAt(dongChon, 4).toString();
            String diaChi=tblTaiKhoan.getValueAt(dongChon, 5).toString();
            Date ngaySinh= (Date) tblTaiKhoan.getValueAt(dongChon, 6);
            String quyen=tblTaiKhoan.getValueAt(dongChon, 7).toString();
            taiKhoanChon=new TaiKhoanObject(maTK, tenDN, matKhau, hoTen, sdt, diaChi, ngaySinh,quyen);
            txtMaTK.setText(maTK);
            txtTenDN.setText(tenDN);
            txtMK.setText(matKhau);
            txtHoTen.setText(hoTen);
            txtSDT.setText(sdt);
            txtDiaChi.setText(diaChi);
            dateNgaySinh.setDate(ngaySinh);
            cboQuyen.setSelectedItem(quyen+"");
        }
    }//GEN-LAST:event_tblTaiKhoanMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy/MM/dd");
        String maTK=txtMaTK.getText();
        if (maTK.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Mã tài khoản không để trống");
            return;
        }
        String tenDN=txtTenDN.getText();
        if (tenDN.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Tên đăng nhập không để trống");
            return;
        }
        String matKhau=txtMK.getText();
        if (matKhau.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu không để trống");
            return;
        }
        String hoTen=txtHoTen.getText();
        if (hoTen.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Họ tên không để trống");
            return;
        }
        String sdt=txtSDT.getText();
        String diaChi=txtDiaChi.getText();
        java.util.Date ns=dateNgaySinh.getDate();
        String ngaySinh=sdf.format(ns)+"";
        if(conned.themTaiKhoan(maTK, tenDN, matKhau, hoTen,  sdt, diaChi, ngaySinh)){
            JOptionPane.showMessageDialog(null, "Thêm tài khoản thành công !");
            loadBang();
        }
        else{
            JOptionPane.showMessageDialog(null, "Thêm tài khoản thất bại !");
            return;
        }
    }//GEN-LAST:event_btnThemActionPerformed

