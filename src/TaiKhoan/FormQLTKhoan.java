    private void loadBang(){
        ArrayList<TaiKhoanObject>dsTaiKhoan=conned.dsTK();
        for(int i=tblTaiKhoan.getRowCount()-1;i>=0; i--){
            dtable.removeRow(i);
        }
        for(TaiKhoanObject tk :dsTaiKhoan){
            Vector<Object> vec=new Vector<>();
            vec.add(tk.getMaTK());
            vec.add(tk.getTenDN());
            vec.add(tk.getMatKhau());
            vec.add(tk.getHoTen());
            vec.add(tk.getSoDT());
            vec.add(tk.getDiaChi());
            vec.add(tk.getNgaySinh());
            vec.add(tk.getQuyen());
            dtable.addRow(vec);
        }
    }

