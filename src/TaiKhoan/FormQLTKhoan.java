    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int cf=JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn xoá mã "+taiKhoanChon.getMaTK()+"?",
                "Xác nhận xoá", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(cf==JOptionPane.OK_OPTION){
            if(conned.xoaTaiKhoan(taiKhoanChon.getMaTK())){
            JOptionPane.showMessageDialog(null, "Xoá tài khoản thành công !");
            loadBang();
        }
        else{
            JOptionPane.showMessageDialog(null, "Xoá tài khoản thất bại !");
            return;
        }
        }
        else{
            return;
        }
    }//GEN-LAST:event_btnXoaActionPerformed

